#include "get_next_line.h"

int		ft_getchar(int fd)
{
	char	buf;
	int		n;

	n = 1;
	if ((n = read(fd, &buf, 1)) == -1)
		return (-1);
	if (n == 0)
		return (0);
	return (buf);
}
